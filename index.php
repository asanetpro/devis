                <h3>La position du monte-escalier dans vos escaliers</h3>
                
                <p>
                    Vous allez probablement penser : qu’importe l’endroit où le monte-escalier est placé, l’important est de monter en toute sécurité. Vous avez raison mais il est également important d’examiner de quel côté le monte-escalier est installé car il est possible de poser le rail côté mur ou côté rampe. Si l’appareil est placé côté rampe, cette dernière devra probablement être retirée et l’escalier sera moins accessibles à vos proches (famille ou aides à domicile). En revanche, si il est placé du côté du mur, l’escalier restera parfaitement accessible et votre cage d’escalier sera préservée : c’est d’ailleurs souvent la solution qui est privilégiée.
                </p>
